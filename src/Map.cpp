Map::Map() {
    water = TextureManager::LoadTexture("../../../ass/water.png");
    sand = TextureManager::LoadTexture("../../../ass/sand.png");
    grass = TextureManager::LoadTexture("../../../ass/grass.png");
    forest = TextureManager::LoadTexture("../../../ass/forest.png");

    LoadMap(lvl1);

    src.x = src.y = 0;
    dest.x = dest.y = 0;

    src.w = 32; dest.w = 16;
    src.h = 32; dest.h = 16;
}

Map::~Map() {}

void Map::LoadMap(short arr[40][60]) {

    for (short row = 0; row < 40; row++) {
        for (short column = 0; column < 60; column++) {
            map[row][column] = arr[row][column];
        }
    }
}

void Map::DrawMap() {
    short type = 0;

    for (short row = 0; row < 40; ++row) {
        for (short column = 0; column < 60; ++column) {

            type = map[row][column];

            dest.x = column * 16;
            dest.y = row * 16;

            switch (type)
            {
            case 0:
                TextureManager::Draw(water, src, dest);
                break;
            case 1:
                TextureManager::Draw(sand, src, dest);
                break;
            case 2:
                TextureManager::Draw(grass, src, dest);
                break;
            default:
                TextureManager::Draw(forest, src, dest);
                break;
            }
        }
    }
}

void Map::paintValue(short x, short y)
{
    //std::cout << "x: " << x << " y: " << y << "\n";

    x /= 16;
    y /= 16;
    
    //std::cout << "col: " << x << " row: " << y << "\n";

    lvl1[y][x] = value;

    LoadMap(lvl1);
}

void Map::printMap()
{
    std::cout << "short lvl[" << 40 << "][" << 60 << "] = {\n";

    for (short j = 0; j < 39; ++j) {
        std::cout << "\t{ ";

        for (short i = 0; i < 60; ++i) {
            std::cout << map[j][i] << ", ";
        }

        std::cout << "},\n";
    }

    std::cout << "\t{ ";
    for (short i = 0; i < 60; ++i) {
        std::cout << map[39][i] << ", ";
    }

    std::cout << "}";

    std::cout << "\n};\n";
}


